from django.contrib import admin
from django.urls import include, path, re_path
from django.conf.urls.static import static
from django.conf import settings

from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

from users.views import MyTokenObtainPairView
from users.views import CustomUserCreate
from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from rest_framework import routers, permissions

schema_view = get_schema_view(
    openapi.Info(
        title="YOGAYO OPEN API 명세서",
        default_version='v1',
        description="부케서비스 OPEN API 입니다.",
        terms_of_service="https://www.google.com/policies/terms/",
        contact=openapi.Contact(email="contact@snippets.local"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('', include('shops.urls')),
    path('flowers/', include('flowers.urls')),
    path('bouquets/', include('bouquets.urls')),
    path('ribbons/', include('ribbons.urls')),
    path('wrappingPapers/', include('wrappingPapers.urls')),
    path('bouquet_order/', include('bouquet_order.urls')),
    # API
    path('api/user/', include('users.urls', namespace='users')),
    path('api-auth/', include('rest_framework.urls', namespace='rest_framework')),
    path('api/token/', MyTokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
    path('api/users/create/',CustomUserCreate.as_view()),

    # SWAGGER
    re_path(r'^swagger(?P<format>\.json|\.yaml)$',
            schema_view.without_ui(cache_timeout=0), name='schema-json'),
    re_path(r'^swagger/$', schema_view.with_ui('swagger',
                                               cache_timeout=0), name='schema-swagger-ui'),
    re_path(r'^redoc/$', schema_view.with_ui('redoc',
                                             cache_timeout=0), name='schema-redoc'),

] + static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)